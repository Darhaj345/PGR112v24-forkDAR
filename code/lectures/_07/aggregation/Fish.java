package lectures._07.aggregation;


public class Fish
{
    //# Fields
    private String name;


    //# Constructors
    public Fish(String name) {
        this.name = name;
    }


    //# Getter-methods
    public String getName() {
        return this.name;
    }


    //# Setter-methods
    private void setName(String value) {
        this.name = value;
    }
}